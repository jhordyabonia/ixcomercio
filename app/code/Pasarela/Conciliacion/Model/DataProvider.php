     * @param string $primaryFieldName
     * @param string $requestFieldName
     * @param CollectionFactory $conciliacionCollectionFactory
     * @param array $meta
     * @param array $data
     */
    public function __construct(
        $name,
        $primaryFieldName,
        $requestFieldName,
        CollectionFactory $conciliacionCollectionFactory,
        array $meta = [],
        array $data = []
    ) {
        $this->collection = $conciliacionCollectionFactory->create();
        parent::__construct($name, $primaryFieldName, $requestFieldName, $meta, $data);
    }

    /**
     * Get data
     *
     * @return array
     */
    public function getData()
    {
        return [];
    }
}
